/**
 * prompts.js
 * Facebook / X 向けコンテンツ変換プロンプト定義
 *
 * 設計思想:
 *   - 同じ元ネタでも「チャネル × 業種」の組み合わせで読者が感じる価値が変わる
 *   - FacebookはSEO的長文で「保存したくなる実用情報」を目指す
 *   - XはフックとなるThreadで「フォローしたくなる専門家」像を構築する
 *   - どちらも「売り込み禁止」「共感ファースト」を原則とする
 */

/** 業種別の悩み・文脈マッピング */
const INDUSTRY_CONTEXT = {
  beauty: {
    label: '美容・エステサロン',
    pain_points: '設備投資・スタッフ採用・閑散期の資金繰り・広告費の回収',
    tone: '寄り添い型・体験談重視・専門用語を避ける',
    keywords: ['エステサロン', '美容室', '開業', '設備費', '集客コスト'],
  },
  food: {
    label: '飲食店・カフェ',
    pain_points: '原材料費高騰・人件費・テナント更新・設備老朽化',
    tone: '実務的・数字で語る・現場感重視',
    keywords: ['飲食店', 'カフェ', '居酒屋', '原価', '運転資金'],
  },
  fashion: {
    label: '服飾・アパレルEC',
    pain_points: '在庫資金・仕入れサイクル・EC初期投資・季節在庫リスク',
    tone: 'スタートアップ的・数字とスピード感重視',
    keywords: ['アパレル', 'EC', '在庫', '仕入れ資金', 'D2C'],
  },
};

/**
 * Facebook 向けプロンプト生成
 * 目標: 300〜500文字、信頼構築、保存・シェアされやすい実用情報
 */
function buildFacebookPrompt(article, industry = 'beauty') {
  const ctx = INDUSTRY_CONTEXT[industry] || INDUSTRY_CONTEXT.beauty;

  return `
あなたは中小企業の資金調達・経営改善を専門とするコンサルタントです。
以下の記事を、${ctx.label}を経営する経営者が「これは自分のことだ」と感じる
Facebook投稿文に変換してください。

## 変換元記事
タイトル: ${article.title}
本文: ${article.body}
出典URL: ${article.source_url}

## 投稿の設計方針
- 文字数: 300〜500文字（改行を活用して読みやすく）
- 冒頭: ${ctx.pain_points} のいずれかに言及し、共感を引き出す一文で始める
- 構成: [共感→問題提起→解決の糸口→行動促進] の4段構成
- トーン: ${ctx.tone}
- 末尾: 「詳しくは無料診断で確認できます」などLINE誘導のCTAを自然に1行加える
  （CTA文は毎回少し表現を変えること）
- 絵文字: 使用禁止
- ハッシュタグ: 投稿文の最後に業種関連タグを5個（例: #エステサロン経営 #美容室開業）

## 禁止事項
- 「今すぐ申し込みを」などの直接的な売り込み表現
- 根拠のない断定（「必ず審査が通ります」等）
- 競合他社への言及

出力: 投稿本文のみ（説明文・前置きは不要）
`.trim();
}

/**
 * X (Twitter) 向けプロンプト生成
 * 目標: 1ツイート目がフック、全5ツイートのスレッド形式
 */
function buildXPrompt(article, industry = 'beauty') {
  const ctx = INDUSTRY_CONTEXT[industry] || INDUSTRY_CONTEXT.beauty;

  return `
あなたは${ctx.label}専門の経営コンサルタントとしてXで発信しています。
以下の記事を元に、経営者がリツイートしたくなる【5ツイートスレッド】を作成してください。

## 変換元記事
タイトル: ${article.title}
本文: ${article.body}

## スレッド設計
ツイート1 (フック): 「〇〇な${ctx.label}経営者へ」「知らないと損する〇〇」形式。
              数字や逆説を使い、続きを読みたくさせる。140文字以内。
ツイート2〜4 (本論): 記事の核心を3つの観点に分解。各140文字以内。
              ${ctx.pain_points} に絡めた具体的なノウハウ。
ツイート5 (CTA): 「気になった方は→ [無料診断を試してみてください]」で締める。
            毎回CTA文の表現を微妙に変えること。URLプレースホルダーは {LINE_URL} を使用。

## フォーマット
各ツイートを「---」で区切って出力する。
番号やラベルは不要。ツイート本文のみ。

## トーン
- 親しみやすい専門家
- 断言を避け「〜の傾向があります」「〜ことが多いです」を使う
- 売り込みゼロ、有益情報を惜しみなく提供するスタンス

出力: スレッド本文のみ
`.trim();
}

/**
 * 10%ランダム揺らぎプロンプト
 * 生成済みテキストをわずかに変化させスパム判定を回避する
 */
function buildRandomizerPrompt(originalText, channel) {
  return `
以下の${channel}投稿文を、意味・内容・トーンを完全に保ちながら、
表現だけを約10%変化させてください。

変化のルール:
- 同義語への置き換え（例:「経営者」→「オーナー」「社長さん」）
- 文頭・文末の言い回しを微調整
- 接続詞・助詞の自然な入れ替え
- 箇条書きの順序をランダムに入れ替えてもよい
- ハッシュタグ・URL・数字・固有名詞は変更しない

元テキスト:
${originalText}

出力: 変化後のテキストのみ（説明不要）
`.trim();
}

module.exports = {
  buildFacebookPrompt,
  buildXPrompt,
  buildRandomizerPrompt,
  INDUSTRY_CONTEXT,
};
